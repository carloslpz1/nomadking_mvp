	FOREIGN KEY("followed_user_id") REFERENCES "users"("id") ON DELETE NO ACTION ON UPDATE CASCADE,
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "likes" (
	"id"	INTEGER,
	"user_id"	INTEGER,
	"post_id"	INTEGER,
	"createdAt"	DATETIME NOT NULL,
	"updatedAt"	DATETIME NOT NULL,
	FOREIGN KEY("post_id") REFERENCES "posts"("id") ON DELETE NO ACTION ON UPDATE CASCADE,
	FOREIGN KEY("user_id") REFERENCES "users"("id") ON DELETE NO ACTION ON UPDATE CASCADE,
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "comments" (
	"id"	INTEGER,
	"content"	TEXT NOT NULL,
	"user_id"	INTEGER,
	"post_id"	INTEGER,
	"createdAt"	DATETIME NOT NULL,
	"updatedAt"	DATETIME NOT NULL,
	FOREIGN KEY("post_id") REFERENCES "posts"("id") ON DELETE NO ACTION ON UPDATE CASCADE,
	FOREIGN KEY("user_id") REFERENCES "users"("id") ON DELETE NO ACTION ON UPDATE CASCADE,
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "chats" (
	"id"	INTEGER,
	"user1_id"	INTEGER,
	"user2_id"	INTEGER,
	"createdAt"	DATETIME NOT NULL,
	"updatedAt"	DATETIME NOT NULL,
	FOREIGN KEY("user2_id") REFERENCES "users"("id") ON DELETE NO ACTION ON UPDATE CASCADE,
	FOREIGN KEY("user1_id") REFERENCES "users"("id") ON DELETE NO ACTION ON UPDATE CASCADE,
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "messages" (
	"id"	INTEGER,
	"content"	VARCHAR(255) NOT NULL,
	"chat_id"	INTEGER,
	"sender_id"	INTEGER,
	"receiver_id"	INTEGER,
	"createdAt"	DATETIME NOT NULL,
	"updatedAt"	DATETIME NOT NULL,
	FOREIGN KEY("chat_id") REFERENCES "chats"("id") ON DELETE NO ACTION ON UPDATE CASCADE,
	FOREIGN KEY("receiver_id") REFERENCES "users"("id") ON DELETE NO ACTION ON UPDATE CASCADE,
	FOREIGN KEY("sender_id") REFERENCES "users"("id") ON DELETE NO ACTION ON UPDATE CASCADE,
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "addresses" (
	"id"	INTEGER,
	"street"	VARCHAR(255) NOT NULL,
	"city"	VARCHAR(255) NOT NULL,
	"state"	VARCHAR(255) NOT NULL,
	"createdAt"	DATETIME NOT NULL,
	"updatedAt"	DATETIME NOT NULL,
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "places" (
	"id"	INTEGER,
	"name"	VARCHAR(255) NOT NULL,
	"description"	TEXT,
	"score_avg"	FLOAT,
	"op_hours"	VARCHAR(255),
	"internet_qua"	TEXT,
	"address_id"	INTEGER,
	"createdAt"	DATETIME NOT NULL,
	"updatedAt"	DATETIME NOT NULL,
	FOREIGN KEY("address_id") REFERENCES "addresses"("id") ON DELETE NO ACTION ON UPDATE CASCADE,
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "phones" (
	"id"	INTEGER,
	"country_code"	VARCHAR(255),
	"number"	VARCHAR(255) NOT NULL,
	"place_id"	INTEGER,
	"createdAt"	DATETIME NOT NULL,
	"updatedAt"	DATETIME NOT NULL,
	FOREIGN KEY("place_id") REFERENCES "places"("id") ON DELETE NO ACTION ON UPDATE CASCADE,
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "places_ratings" (
	"id"	INTEGER,
	"score"	INTEGER NOT NULL,
	"comment"	TEXT,
	"place_id"	INTEGER,
	"user_id"	INTEGER,
	"createdAt"	DATETIME NOT NULL,
	"updatedAt"	DATETIME NOT NULL,
	FOREIGN KEY("user_id") REFERENCES "users"("id") ON DELETE NO ACTION ON UPDATE CASCADE,
	FOREIGN KEY("place_id") REFERENCES "places"("id") ON DELETE NO ACTION ON UPDATE CASCADE,
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "plans" (
	"id"	INTEGER,
	"name"	VARCHAR(255) NOT NULL,
	"description"	TEXT,
	"price"	FLOAT NOT NULL,
	"duration"	INTEGER NOT NULL,
	"createdAt"	DATETIME NOT NULL,
	"updatedAt"	DATETIME NOT NULL,
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "subscriptions" (
	"id"	INTEGER,
	"start_date"	DATE NOT NULL,
	"end_date"	DATE NOT NULL,
	"user_id"	INTEGER,
	"plan_id"	INTEGER,
	"createdAt"	DATETIME NOT NULL,
	"updatedAt"	DATETIME NOT NULL,
	FOREIGN KEY("plan_id") REFERENCES "plans"("id") ON DELETE NO ACTION ON UPDATE CASCADE,
	FOREIGN KEY("user_id") REFERENCES "users"("id") ON DELETE NO ACTION ON UPDATE CASCADE,
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "transaction_types" (
	"id"	INTEGER,
	"name"	VARCHAR(255) NOT NULL,
	"createdAt"	DATETIME NOT NULL,
	"updatedAt"	DATETIME NOT NULL,
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "transactions" (
	"id"	INTEGER,
	"amount"	FLOAT NOT NULL,
	"user_id"	INTEGER,
	"type"	INTEGER,
